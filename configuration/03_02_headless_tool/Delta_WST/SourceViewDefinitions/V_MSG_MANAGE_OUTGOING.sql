CREATE OR REPLACE FORCE EDITIONABLE VIEW "V_MSG_MANAGE_OUTGOING" ("MSG_TYPE_ID", "TO_PARAM_SET", "TO_PARAM", "DAYTIME", "MESSAGE_DISTRIBUTION_NO", "FUNCTIONAL_AREA_ID", "REV_TEXT", "CREATED_BY", "CREATED_DATE", "LAST_UPDATED_BY", "LAST_UPDATED_DATE", "REV_NO", "RECORD_STATUS") AS 
  SELECT
           MD.OBJECT_ID AS MSG_TYPE_ID,
           ECBP_MESSAGING.GetParamSetName(MD.MESSAGE_DISTRIBUTION_NO) AS TO_PARAM_SET,
           ECBP_MESSAGING.GetParamSetValue(MD.MESSAGE_DISTRIBUTION_NO) AS TO_PARAM,
           SD.DAYTIME AS DAYTIME,
           MD.MESSAGE_DISTRIBUTION_NO AS MESSAGE_DISTRIBUTION_NO,
           MDFV.FUNCTIONAL_AREA_ID,
           MD.REV_TEXT,
           MD.CREATED_BY,
           MD.CREATED_DATE,
           MD.LAST_UPDATED_BY,
           MD.LAST_UPDATED_DATE,
           MD.REV_NO,
           'P' AS RECORD_STATUS
      FROM MESSAGE_DISTRIBUTION MD,
           MESSAGE_DEF_VERSION MDFV,
           SYSTEM_DAYS SD
     WHERE MD.OBJECT_ID = MDFV.OBJECT_ID
       AND MDFV.DAYTIME <= SD.DAYTIME AND (MDFV.END_DATE > SD.DAYTIME OR MDFV.END_DATE IS NULL)