CREATE OR REPLACE FORCE EDITIONABLE VIEW "V_CURR_UNIT" ("CURR_UNIT_TYPE", "CURR_UNIT_ID", "CODE", "NAME", "DAYTIME", "END_DATE", "RECORD_STATUS", "CREATED_BY", "CREATED_DATE", "LAST_UPDATED_BY", "LAST_UPDATED_DATE", "REV_NO", "REV_TEXT") AS 
  SELECT
    CLASS_NAME AS CURR_UNIT_TYPE,
    OBJECT_ID AS CURR_UNIT_ID,
    CODE AS CODE,
    NAME,
    DAYTIME,
    END_DATE,
    NULL AS RECORD_STATUS,
    NULL AS CREATED_BY,
    NULL AS CREATED_DATE,
    NULL AS LAST_UPDATED_BY,
    NULL AS LAST_UPDATED_DATE,
    NULL AS REV_NO,
    NULL AS REV_TEXT
FROM OV_CURRENCY

UNION ALL

SELECT
    TABLE_CLASS_NAME,
    UNIT,
    UNIT,
    LABEL,
    TO_DATE('1900-01-01T00:00:00', 'YYYY-MM-DD"T"HH24:MI:SS'),
    NULL,
    NULL AS RECORD_STATUS,
    NULL AS CREATED_BY,
    NULL AS CREATED_DATE,
    NULL AS LAST_UPDATED_BY,
    NULL AS LAST_UPDATED_DATE,
    NULL AS REV_NO,
    NULL AS REV_TEXT
FROM TV_UNIT