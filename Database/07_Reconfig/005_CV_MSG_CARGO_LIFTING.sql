
  CREATE OR REPLACE FORCE EDITIONABLE VIEW "CV_MSG_CARGO_LIFTING" ("OBJECT_ID", "LIFTING_ACCOUNT_ID", "PROFIT_CENTRE_ID", "CARRIER_ID", "NOM_FIRM_DATE", "BL_DATE", "CARGO_NO", "CARGO_NAME", "PARCEL_NO", "NOM_SPLIT_PCT", "COMB_IND", "EXCESS_LIFTING_IND", "NOM_FIRM_DATE_TIME", "GRS_VOL_NOMINATED", "BL_DATE_TIME", "EXPORT_QTY", "EXPORT_DAYTIME", "RECORD_STATUS", "CREATED_BY", "CREATED_DATE", "LAST_UPDATED_BY", "LAST_UPDATED_DATE", "REV_NO", "REV_TEXT", "APPROVAL_BY", "APPROVAL_DATE", "APPROVAL_STATE") AS 
  (SELECT NOM.OBJECT_ID,
           NOM.LIFTING_ACCOUNT_ID,
           EC_LIFTING_ACCOUNT.PROFIT_CENTRE_ID (NOM.LIFTING_ACCOUNT_ID)
              AS PROFIT_CENTRE_ID,
           NOM.CARRIER_ID,
           NOM.NOM_FIRM_DATE,
           NOM.BL_DATE,
           NOM.CARGO_NO,
           CAR.CARGO_NAME,
           NOM.PARCEL_NO,
           NOM.VALUE_11,
           NOM.TEXT_12 AS COMB_IND,
           NOM.TEXT_13 AS EXCESS_LIFTING_IND,
           NOM.DATE_8,
           NOM.GRS_VOL_NOMINATED,
           NOM.DATE_9,
           EXP.EXPORT_QTY,
           EXP.DAYTIME,
           NOM.RECORD_STATUS,
           NOM.CREATED_BY,
           NOM.CREATED_DATE,
           NOM.LAST_UPDATED_BY,
           NOM.LAST_UPDATED_DATE,
           NOM.REV_NO,
           NOM.REV_TEXT,
           NOM.APPROVAL_BY,
           NOM.APPROVAL_DATE,
           NOM.APPROVAL_STATE
      FROM STORAGE_LIFT_NOMINATION NOM
           INNER JOIN CARGO_TRANSPORT CAR ON NOM.CARGO_NO = CAR.CARGO_NO
           LEFT OUTER JOIN STOR_PERIOD_EXPORT_STATUS EXP
              ON NOM.CARGO_NO = EXP.CARGO_NO);
