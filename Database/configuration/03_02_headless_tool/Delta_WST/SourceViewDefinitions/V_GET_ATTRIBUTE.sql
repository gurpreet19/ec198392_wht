CREATE OR REPLACE FORCE EDITIONABLE VIEW "V_GET_ATTRIBUTE" ("ATTRIBUTE", "VIEWLABELHEAD_LABEL", "CLASS_NAME", "RECORD_STATUS", "CREATED_BY", "CREATED_DATE", "LAST_UPDATED_BY", "LAST_UPDATED_DATE", "REV_NO", "REV_TEXT") AS 
  SELECT A.ATTRIBUTE_NAME ATTRIBUTE,
       EcDp_ClassMeta_Cnfg.getStaticViewlabelhead(A.CLASS_NAME, A.ATTRIBUTE_NAME)||' '||EcDp_ClassMeta_Cnfg.getLabel(A.CLASS_NAME, A.ATTRIBUTE_NAME) VIEWLABELHEAD_LABEL,
       A.CLASS_NAME,
       NULL AS RECORD_STATUS,
       NULL AS CREATED_BY,
       NULL AS CREATED_DATE,
       NULL AS LAST_UPDATED_BY,
       NULL AS LAST_UPDATED_DATE,
       NULL AS REV_NO,
       NULL AS REV_TEXT
  FROM CLASS_ATTRIBUTE_CNFG A
  WHERE EcDp_ClassMeta_Cnfg.isDisabled(A.CLASS_NAME, A.ATTRIBUTE_NAME) = 'N'
   AND (EcDp_ClassMeta_Cnfg.getUomCode(A.CLASS_NAME, A.ATTRIBUTE_NAME) IS NOT NULL OR EcDp_ClassMeta_Cnfg.getStaticViewlabelhead(A.CLASS_NAME, A.ATTRIBUTE_NAME) IS NOT NULL)