CREATE OR REPLACE FORCE EDITIONABLE VIEW "V_MSG_GEN_OUTGOING" ("MESSAGE_GROUP_ID", "MSG_TYPE_ID", "TO_PARAM_SET", "TO_PARAM", "MSG_DAYTIME", "GRP_DAYTIME", "MSG_END_DATE", "GRP_END_DATE", "MESSAGE_DISTRIBUTION_NO", "FUNCTIONAL_AREA_ID", "REV_TEXT", "CREATED_BY", "CREATED_DATE", "LAST_UPDATED_BY", "LAST_UPDATED_DATE", "REV_NO", "RECORD_STATUS") AS 
  SELECT
           MGC.MESSAGE_GROUP_ID,
           MD.OBJECT_ID AS MSG_TYPE_ID,
           ECBP_MESSAGING.GetParamSetName(MD.MESSAGE_DISTRIBUTION_NO) AS TO_PARAM_SET,
           ECBP_MESSAGING.GetParamSetValue(MD.MESSAGE_DISTRIBUTION_NO) AS TO_PARAM,
           MDFV.DAYTIME AS MSG_DAYTIME,
           MGV.DAYTIME AS GRP_DAYTIME,
           MDFV.END_DATE AS MSG_END_DATE,
           MGV.END_DATE AS GRP_END_DATE,
           MD.MESSAGE_DISTRIBUTION_NO AS MESSAGE_DISTRIBUTION_NO,
           MDFV.FUNCTIONAL_AREA_ID,
           MGC.REV_TEXT,
           MGC.CREATED_BY,
           MGC.CREATED_DATE,
           MGC.LAST_UPDATED_BY,
           MGC.LAST_UPDATED_DATE,
           MGC.REV_NO,
           'P' AS RECORD_STATUS
      FROM MESSAGE_DISTRIBUTION MD,
           MESSAGE_DEF_VERSION MDFV,
           MESSAGE_GROUP_CONN MGC,
           MESSAGE_GROUP_VERSION MGV
     WHERE MD.MESSAGE_DISTRIBUTION_NO = MGC.MESSAGE_DISTRIBUTION_NO
       AND MGC.MESSAGE_GROUP_ID = MGV.OBJECT_ID
       AND MD.OBJECT_ID = MDFV.OBJECT_ID