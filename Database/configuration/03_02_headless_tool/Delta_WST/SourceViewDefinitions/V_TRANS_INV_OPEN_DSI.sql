CREATE OR REPLACE FORCE EDITIONABLE VIEW "V_TRANS_INV_OPEN_DSI" ("DAYTIME", "OBJECT_ID", "DIMENSION_TAG", "CALC_RUN_NO", "RECORD_STATUS", "CREATED_BY", "CREATED_DATE", "LAST_UPDATED_BY", "LAST_UPDATED_DATE", "REV_NO", "REV_TEXT") AS 
  SELECT DISTINCT DAYTIME, OBJECT_ID, DIMENSION_TAG, CALC_RUN_NO,
       NULL RECORD_STATUS,
       NULL CREATED_BY ,
       NULL CREATED_DATE ,
       NULL LAST_UPDATED_BY ,
       NULL LAST_UPDATED_DATE  ,
       NULL REV_NO  ,
       NULL REV_TEXT
       FROM TRANS_INVENTORY_BALANCE X
GROUP BY OBJECT_ID,X.CALC_RUN_NO,DIMENSION_TAG,DAYTIME