-- Add values to PRICE_ROUNDING_RULE column
DECLARE 
HASENTRY NUMBER;
BEGIN
   SELECT COUNT(*) INTO HASENTRY FROM USER_TRIGGERS 
	 WHERE TRIGGER_NAME = 'IU_PRODUCT_PRICE_VERSION'; 
	 
	 IF HASENTRY > 0 THEN 
	 EXECUTE IMMEDIATE 'ALTER TRIGGER IU_PRODUCT_PRICE_VERSION DISABLE';
	 END IF;
	 
	UPDATE PRODUCT_PRICE_VERSION SET PRICE_ROUNDING_RULE ='BY_PRICE_ELEMENT';
	
	 IF HASENTRY > 0 THEN 
	 EXECUTE IMMEDIATE 'ALTER TRIGGER IU_PRODUCT_PRICE_VERSION ENABLE';
	 END IF; 	
	 
EXCEPTION
WHEN OTHERS THEN 	
	IF HASENTRY > 0 THEN 
        EXECUTE IMMEDIATE 'ALTER TRIGGER IU_PRODUCT_PRICE_VERSION ENABLE';
    END IF;    
        raise_application_error(-20000,'ERROR: Some Other fatal error occured :- '||SQLERRM);
END;
