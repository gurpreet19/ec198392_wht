DECLARE 
HASENTRY NUMBER;
BEGIN
	SELECT COUNT(*) INTO HASENTRY FROM USER_TRIGGERS 
		WHERE TRIGGER_NAME = 'IU_OBJLOC_DAY_NOMINATION'; 
		IF HASENTRY > 0 THEN 
		EXECUTE IMMEDIATE 'ALTER TRIGGER IU_OBJLOC_DAY_NOMINATION DISABLE';
		END IF;
		
	UPDATE OBJLOC_DAY_NOMINATION SET NOMINATION_SEQ = EcDp_System_Key.assignNextNumber('OBJLOC_DAY_NOMINATION');
	
	SELECT COUNT(*) INTO HASENTRY FROM USER_TRIGGERS 
		WHERE TRIGGER_NAME = 'IU_OBJLOC_DAY_NOMINATION'; 
		IF HASENTRY > 0 THEN 
		EXECUTE IMMEDIATE 'ALTER TRIGGER IU_OBJLOC_DAY_NOMINATION ENABLE';
		END IF;
	
	EXCEPTION
   WHEN OTHERS 
     THEN
       IF HASENTRY > 0 THEN 
           EXECUTE IMMEDIATE 'ALTER TRIGGER IU_OBJLOC_DAY_NOMINATION ENABLE';
       END IF;
        
        raise_application_error(-20000,
                            'ERROR: Some Other fatal error occured :- '||SQLERRM);
END;
