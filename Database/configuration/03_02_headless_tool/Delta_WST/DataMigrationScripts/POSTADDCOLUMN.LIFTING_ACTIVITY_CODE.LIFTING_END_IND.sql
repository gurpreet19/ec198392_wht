-- move data 
--	UPDATE LIFTING_ACTIVITY_CODE SET LIFTING_END_IND = LIFTING_END;
DECLARE 
HASENTRY NUMBER;
BEGIN
	SELECT COUNT(*) INTO HASENTRY FROM USER_TRIGGERS 
		WHERE TRIGGER_NAME = 'IU_LIFTING_ACTIVITY_CODE'; 
		IF HASENTRY > 0 THEN 
		EXECUTE IMMEDIATE 'ALTER TRIGGER IU_LIFTING_ACTIVITY_CODE DISABLE';
		END IF;
		
	FOR X IN ( SELECT COUNT(*) CNT FROM DUAL WHERE EXISTS 
		( SELECT 1 FROM USER_TAB_COLUMNS WHERE TABLE_NAME = 'LIFTING_ACTIVITY_CODE' AND COLUMN_NAME = 'LIFTING_END' ) ) LOOP
		IF ( X.CNT = 1 ) THEN
		EXECUTE IMMEDIATE 'UPDATE LIFTING_ACTIVITY_CODE SET LIFTING_END_IND = LIFTING_END';
		END IF;
	END LOOP;
  
  	SELECT COUNT(*) INTO HASENTRY FROM USER_TRIGGERS 
		WHERE TRIGGER_NAME = 'IU_LIFTING_ACTIVITY_CODE'; 
		IF HASENTRY > 0 THEN 
		EXECUTE IMMEDIATE 'ALTER TRIGGER IU_LIFTING_ACTIVITY_CODE ENABLE';
		END IF;
EXCEPTION
   WHEN OTHERS 
     THEN
       
     IF HASENTRY > 0 THEN 
           EXECUTE IMMEDIATE 'ALTER TRIGGER IU_LIFTING_ACTIVITY_CODE ENABLE';
       END IF;
        
        raise_application_error(-20000,
                            'ERROR: Some Other fatal error occured :- '||SQLERRM);


END;
