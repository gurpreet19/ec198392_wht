CREATE OR REPLACE EDITIONABLE TRIGGER "U_V_TREND_ANALYSIS_CONFIG" 
  INSTEAD OF UPDATE ON V_TREND_ANALYSIS_CONFIG
  FOR EACH ROW
BEGIN

  UPDATE PWEL_RESULT
     SET TREND_ANALYSIS_IND = :NEW.TREND_ANALYSIS_IND,
         TREND_RESET_IND    = :NEW.TREND_RESET_IND
   WHERE OBJECT_ID = :NEW.OBJECT_ID
     AND RESULT_NO = :NEW.RESULT_NO;

END;
